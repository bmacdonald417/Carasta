/** Normalize zip to 5-digit string. */
function normalizeZip(zip: string): string {
  const s = String(zip).trim().replace(/\D/g, "").slice(0, 5);
  return s.length === 5 ? s : "";
}

/**
 * Get lat/lng for a US zip code. Returns null if not found.
 * Extend ZIP_COORDS or integrate a geocoding API for full coverage.
 */
